package ex1;

/**
 * Classe réalisant des opérations arythmétiques.
 * 
 * @author hmerciol
 *
 */
public class Calcul {

	/**
	 * Méthode qui retourne le résultat d'une addition.
	 * 
	 * @param a
	 *            paramètre à additionner
	 * @param b
	 *            deuxième paramètre à additionner
	 * @return int le résultat de a+b
	 */
	public int addition(int a, int b) {
		return a + b;
	}

	/**
	 * Méthode qui retourne le résultat d'une soustraction.
	 * 
	 * @param a
	 *            paramètre à soustraire
	 * @param b
	 *            paramètre qui est soustrait
	 * @return int le résultat de a-b
	 */
	public int soustraction(int a, int b) {
		return a - b;
	}
}
